let b = ["this", "is", "an", "array"]; //initializing array

console.log("Original List");
console.log(b);

b.push("end"); //push to add the elements to the end

console.log("Array Push");
console.log(b);

b.pop(); //pop to remove elements from the end
console.log("Array pop");
console.log(b);

b.unshift("Hello"); //array unshifting to add to first
console.log("Array unShift");
console.log(b);

b.shift(); //array shifting operation to remove from first
console.log("Array SHift");
console.log(b);
